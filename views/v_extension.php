<script>
    $(document).ready(function(){
        $("#filterlayanan").on("change", function(){
            var filterval = $(this).val();
            if(filterval == '') {
                $(".EXT").show();
                return false;
            }
            filterval = filterval.split(";");
            
            var ruangnama = $(".ruang-nama");
            var matchArr = [];
            $(".EXT").hide();
            ruangnama.each(function (index, elem) {
                var nama = $(elem).html();
                nama = nama.toUpperCase();
                for (let index = 0; index < filterval.length; index++) {
                    const element = filterval[index].toUpperCase();
                    var re = new RegExp(element, 'g');
                    if(nama.match(re)) {
                        matchArr.push(nama);
                    }
                }
            });

            for (let index = 0; index < matchArr.length; index++) {
                const element = matchArr[index];
                var klass = element.split(' ').join('-');
                var re = new RegExp('JP-3', 'g');
                if(klass.match(re)) {
                    $(".JP-3").show(); 
                } else {
                    $("."+klass).show();   
                }
            }
        });
    });
</script>